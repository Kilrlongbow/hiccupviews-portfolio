#!/usr/bin/env node
//
// generate-image-data.mjs
// Scans public/photos/{home,portraits,events}/ (produced by optimize-images.sh)
// and writes src/data/generatedPhotos.ts. Run after optimize-images.
//
import { readdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const PHOTOS = resolve(__dirname, '../public/photos');
const OUT = resolve(__dirname, '../src/data/generatedPhotos.ts');

// Collect unique base names from a folder by stripping the size suffix.
function baseNames(folder) {
  const names = new Set();
  for (const file of readdirSync(resolve(PHOTOS, folder))) {
    const m = file.match(/^(.*)-(?:thumb|grid|full)\.jpg$/i);
    if (m) names.add(m[1]);
  }
  return [...names].sort();
}

const home = baseNames('home').map((id) => ({
  id,
  src: `/photos/home/${id}-thumb.jpg`,
  full: `/photos/home/${id}-full.jpg`,
  alt: `Hiccupviews photography — ${id}`,
}));

const portraits = baseNames('portraits').map((id) => ({
  src: `/photos/portraits/${id}-grid.jpg`,
  full: `/photos/portraits/${id}-full.jpg`,
  alt: `Hiccupviews portrait — ${id}`,
}));

const events = baseNames('events').map((id) => ({
  src: `/photos/events/${id}-grid.jpg`,
  full: `/photos/events/${id}-full.jpg`,
  alt: `Hiccupviews event — ${id}`,
}));

const lines = [
  '// AUTO-GENERATED by scripts/generate-image-data.mjs — do not edit by hand.',
  '// Regenerate with `npm run optimize-images`.',
  "import type { ImageData } from '../components/SphereImageGrid';",
  "import type { AlbumPhoto } from './albums';",
  '',
  `// ${home.length} homepage-sphere photos (square thumb + full).`,
  `export const homePhotos: ImageData[] = ${JSON.stringify(home, null, 2)};`,
  '',
  `// ${portraits.length} portrait photos (grid + full).`,
  `export const portraitPhotos: AlbumPhoto[] = ${JSON.stringify(portraits, null, 2)};`,
  '',
  `// ${events.length} event photos (grid + full).`,
  `export const eventPhotos: AlbumPhoto[] = ${JSON.stringify(events, null, 2)};`,
  '',
];

writeFileSync(OUT, lines.join('\n'));
console.log(
  `Wrote ${OUT}\n  home: ${home.length}  portraits: ${portraits.length}  events: ${events.length}`,
);
